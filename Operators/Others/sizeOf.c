// sizeof Operators //

#include <stdio.h>
int main()
{
    int a;
    printf("%d\n", sizeof(a)); // sizeof integer //

    char ch;
    printf("%d\n", sizeof(ch)); // sizeof char //

    float f;
    printf("%d\n", sizeof(f)); // sizeof float //

    double d;
    printf("%d\n", sizeof(d)); // sizeof double //

    return 0;
}